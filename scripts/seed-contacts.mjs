async function seed() {
  let totalAdded = 0;
  const errors = [];

  for (const [companyKey, contacts] of Object.entries(ORG_DATA)) {
    const prospectId = COMPANIES[companyKey];
    if (!prospectId) {
      console.log(`⚠ No ID found for ${companyKey}`);
      continue;
    }

    const payload = contacts.map(([name, categoryRaw, title]) => ({
      name,
      categoryRaw,
      title,
    }));

    try {
      const res = await fetch(`${BASE}/prospects/${prospectId}/contacts/bulk`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ contacts: payload, replace: true }),
      });
      const data = await res.json();
      if (data.error) throw new Error(data.error);
      console.log(`✓ ${companyKey}: ${data.added} contacts`);
      totalAdded += data.added;
    } catch (e) {
      console.error(`✗ ${companyKey}: ${e.message}`);
      errors.push({ company: companyKey, error: e.message });
    }
  }

  console.log(`\n✓ Done. ${totalAdded} contacts seeded across ${Object.keys(ORG_DATA).length} companies.`);
  if (errors.length) console.error("Errors:", errors);
}

seed().catch(console.error);
